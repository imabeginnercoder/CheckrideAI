-- Run this in the Supabase SQL editor after enabling Email auth.
-- It adds per-user ownership to PPL Prep data and enables row-level security.

create table if not exists public.quiz_scores (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  score integer not null,
  total_questions integer not null,
  category text not null
);

create table if not exists public.question_results (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  question_id text not null,
  category text not null,
  correct boolean not null,
  session_id text not null
);

create table if not exists public.checklist_items (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  item_id text not null
);

alter table public.quiz_scores
  add column if not exists user_id uuid references auth.users(id) on delete cascade;

alter table public.question_results
  add column if not exists user_id uuid references auth.users(id) on delete cascade;

alter table public.checklist_items
  add column if not exists user_id uuid references auth.users(id) on delete cascade;

create table if not exists public.oral_sessions (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  user_id uuid not null references auth.users(id) on delete cascade,
  mode text not null,
  question_count integer not null,
  transcript jsonb not null,
  summary text not null
);

create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  display_name text,
  checkride_date date,
  preferred_aircraft text not null default 'Cessna 172S',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

alter table public.profiles add column if not exists display_name text;
alter table public.profiles add column if not exists checkride_date date;
alter table public.profiles add column if not exists preferred_aircraft text not null default 'Cessna 172S';
alter table public.profiles add column if not exists created_at timestamptz not null default now();
alter table public.profiles add column if not exists updated_at timestamptz not null default now();

