create table if not exists public.ai_usage (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  user_id uuid not null references auth.users(id) on delete cascade,
  session_key uuid,
  request_id text not null,
  request_kind text not null default 'oral_turn',
  model text not null,
  input_tokens integer not null default 0,
  output_tokens integer not null default 0,
  cache_creation_input_tokens integer not null default 0,
  cache_read_input_tokens integer not null default 0,
  estimated_cost_microusd bigint not null default 0
);

create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

drop trigger if exists profiles_set_updated_at on public.profiles;
create trigger profiles_set_updated_at
  before update on public.profiles
  for each row execute function public.set_updated_at();

create or replace function public.handle_new_user_profile()
returns trigger
language plpgsql
security definer set search_path = ''
as $$
begin
  insert into public.profiles (id, display_name, checkride_date, preferred_aircraft)
  values (
    new.id,
    nullif(new.raw_user_meta_data ->> 'display_name', ''),
    (nullif(new.raw_user_meta_data ->> 'checkride_date', ''))::date,
    coalesce(nullif(new.raw_user_meta_data ->> 'preferred_aircraft', ''), 'Cessna 172S')
  )
  on conflict (id) do nothing;
  return new;
end;
$$;

drop trigger if exists on_auth_user_created_profile on auth.users;
create trigger on_auth_user_created_profile
  after insert on auth.users
  for each row execute function public.handle_new_user_profile();

insert into public.profiles (id, display_name, preferred_aircraft)
select
  id,
  nullif(raw_user_meta_data ->> 'display_name', ''),
  coalesce(nullif(raw_user_meta_data ->> 'preferred_aircraft', ''), 'Cessna 172S')
from auth.users
on conflict (id) do nothing;

